import { getProductBySlug } from "@/lib/actions/product.actions";
import { notFound } from "next/navigation";
import { Badge } from "@/components/ui/badge";
import { Button } from "@/components/ui/button";
import { Card, CardContent } from "@/components/ui/card";
import ProductPrice from "@/components/shared/product/product-price";
import ProductImages from "@/components/shared/product/product-images";

const ProductDetailsPage = async (props: {
  params: Promise<{ slug: string }>;
}) => {
  const { slug } = await props.params;
  const product = await getProductBySlug(slug);
  if (!product) {
    return notFound();
  }

  return (
    <section>
      <div className="grid grid-cols-1 md:grid-cols-5">
        <div className="col-span-2">
          <ProductImages images={product.images}/>
        </div>
        {/* detailed columns */}
        <div className="col-span-2 p-5">
          <div className="flex flex-col gap-6">
            <p>
              {product.brand} {product.category}
            </p>
            <h3 className="h3-bold">{product.name}</h3>
            <p>{product.rating.toString()} of {product.numReviews} Reviews</p>
            <div className="flex flex-col sm:flex-row sm:items-center">
                <ProductPrice value={Number(product.price)} className="bg-green-100 rounded-full w-24 text-green-700 px-5"/>
            </div>
          </div>
          <div className="mt-10">
            <p className="font-semibold">Description</p>
            <p>{product.description}</p>
          </div>
        </div>
        {/* action column */}
        <div>
            <Card>
                <CardContent className="p-4">
                     <div className="mb2 flex justify-between">
                        <div>Price</div>
                        <div>
                            <ProductPrice value={Number(product.price)}/>
                        </div>
                     </div>

                     <div className="mb2 flex justify-between">
                        <div>status</div>
                        <div>
                         {product.stock>0?<Badge variant="default">In Stock</Badge >:<Badge variant="destructive">Out Of Stock</Badge>}
                        </div>
                     </div>
                     {product.stock>0&&<div className="flex-center mt-10">
                        <Button className="w-full">Add To Cart</Button>
                        </div>}
                </CardContent>
            </Card>
        </div>
      </div>
    </section>
  );
};

export default ProductDetailsPage;
